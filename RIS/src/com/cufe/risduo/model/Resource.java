package com.cufe.risduo.model;

public class Resource {
	
	private String name;
	private Integer id;
	
	
	public Resource() {
		
	}


	public Integer getId() {
		return id;
	}


	public void setId(Integer id) {
		this.id = id;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}
	


	
}
